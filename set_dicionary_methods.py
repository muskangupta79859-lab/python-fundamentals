# Practice: Set and Dictionary Methods

# Set example using operations
set_a = {1, 2, 3}
set_b = {3, 4, 5}

print("Set A:", set_a)
print("Set B:", set_b)

print("Union:", set_a.union(set_b))
print("Intersection:", set_a.intersection(set_b))

# Dictionary example using methods
marks = {
    "Maths": 85,
    "Science": 90,
    "English": 78
}

print("Subjects:", marks.keys())
print("Marks:", marks.values())
print("All data:", marks.items())
