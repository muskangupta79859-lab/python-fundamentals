text = " Python Programming Language "

print("Original String:")
print(text)

# Convert to lowercase
print("\nLowercase:")
print(text.lower())

# Convert to uppercase
print("\nUppercase:")
print(text.upper())

# Remove extra spaces
print("\nAfter strip:")
print(text.strip())

# Replace word
print("\nAfter replace:")
print(text.replace("Python","Core Python"))

# Split string
print("\nAfter split:")
print(text.split())

# Length of string
print("\nLength of string:")
print(len(text))
